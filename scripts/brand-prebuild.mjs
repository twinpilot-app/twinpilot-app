#!/usr/bin/env node
/**
 * Brand prebuild — runs before `next dev` / `next build` / on `npm install`.
 *
 * Two jobs:
 *   1. Copy `branding/packs/<BRAND_ID>/` → `public/brand/` so Next serves
 *      the active brand's assets under `/brand/*`.
 *   2. Regenerate `lib/brand.active.ts` from `branding/configs/<BRAND_ID>.json`
 *      so `lib/brand.ts` imports the active brand as a statically-typed object
 *      (bundled on both server and client).
 *
 * Defaults to BRAND_ID=twinpilot. Override with:
 *   BRAND_ID=twinpilot npm run brand:prepare
 */
import { cpSync, existsSync, mkdirSync, readdirSync, readFileSync, rmSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");
const BRAND_ID = process.env.BRAND_ID || "twinpilot";

const CONFIG_PATH = join(ROOT, "branding", "configs", `${BRAND_ID}.json`);
const PACK_DIR = join(ROOT, "branding", "packs", BRAND_ID);
const PUBLIC_BRAND_DIR = join(ROOT, "public", "brand");
const ACTIVE_TS_PATH = join(ROOT, "lib", "brand.active.ts");
const MANIFEST_PATH = join(ROOT, "public", "manifest.json");
const HELPDOCS_SRC = join(ROOT, "content", "helpdocs");
const HELPDOCS_DEST = join(ROOT, "public", "helpdocs");
const LEGAL_SRC = join(ROOT, "content", "legal");
const LEGAL_DEST = join(ROOT, "public", "legal");
const LANDING_SRC = join(PACK_DIR, "web", "landing.html");
const LANDING_DEST = join(ROOT, "public", "landing.html");

if (!existsSync(CONFIG_PATH)) {
  console.error(`[brand-prebuild] Config not found: ${CONFIG_PATH}`);
  console.error(`[brand-prebuild] Expected BRAND_ID to match a file in branding/configs/.`);
  process.exit(1);
}

if (!existsSync(PACK_DIR)) {
  console.error(`[brand-prebuild] Pack not found: ${PACK_DIR}`);
  process.exit(1);
}

const config = JSON.parse(readFileSync(CONFIG_PATH, "utf8"));

if (existsSync(PUBLIC_BRAND_DIR)) rmSync(PUBLIC_BRAND_DIR, { recursive: true, force: true });
mkdirSync(PUBLIC_BRAND_DIR, { recursive: true });
cpSync(PACK_DIR, PUBLIC_BRAND_DIR, { recursive: true });

const header = `// GENERATED — do not edit by hand.
// Regenerated by scripts/brand-prebuild.mjs from branding/configs/${BRAND_ID}.json.
// Committed with BRAND_ID=twinpilot defaults so a fresh clone builds before the
// prebuild hook runs. The sync job for each brand regenerates this file with
// the active brand's config before pushing to the Vercel target repo.

import type { Brand } from "./brand-types";

`;

const body = `const brand: Brand = ${JSON.stringify(config, null, 2)};

export default brand;
`;

writeFileSync(ACTIVE_TS_PATH, header + body, "utf8");

const manifest = {
  name: `${config.name} — Pipeline`,
  short_name: config.shortName,
  description: config.tagline,
  start_url: "/",
  scope: "/",
  display: "standalone",
  orientation: "portrait-primary",
  background_color: "#0a0e1a",
  theme_color: config.theme.themeColor,
  categories: ["productivity", "developer"],
  icons: [
    { src: config.assets.logoMark, sizes: "192x192", type: "image/svg+xml", purpose: "any" },
    { src: config.assets.logoMark, sizes: "512x512", type: "image/svg+xml", purpose: "any maskable" },
  ],
  shortcuts: [
    { name: "Dashboard", url: "/?view=dashboard", description: "Live project status" },
    { name: "Pending Gates", url: "/?view=gates", description: "Human gates awaiting approval" },
  ],
};
writeFileSync(MANIFEST_PATH, JSON.stringify(manifest, null, 2) + "\n", "utf8");

function renderBrandTokens(text) {
  return text.replace(/\{\{brand\.([a-zA-Z0-9_.]+)\}\}/g, (match, path) => {
    const parts = path.split(".");
    let val = config;
    for (const p of parts) val = val?.[p];
    return typeof val === "string" ? val : match;
  });
}

function renderDir(srcDir, destDir) {
  if (existsSync(destDir)) rmSync(destDir, { recursive: true, force: true });
  mkdirSync(destDir, { recursive: true });
  if (!existsSync(srcDir)) return;
  for (const filename of readdirSync(srcDir)) {
    const src = join(srcDir, filename);
    const dst = join(destDir, filename);
    if (filename.endsWith(".md")) {
      writeFileSync(dst, renderBrandTokens(readFileSync(src, "utf8")), "utf8");
    } else {
      cpSync(src, dst);
    }
  }
}

renderDir(HELPDOCS_SRC, HELPDOCS_DEST);
renderDir(LEGAL_SRC, LEGAL_DEST);

if (existsSync(LANDING_SRC)) {
  // Landing is authored inside pack/web/ and references siblings as "../colors/…";
  // when served at /landing.html, rewrite those to absolute /brand/ paths.
  const rewritten = readFileSync(LANDING_SRC, "utf8")
    .replace(/(["'(])\.\.\/([a-zA-Z0-9_\-./]+)/g, "$1/brand/$2");
  writeFileSync(LANDING_DEST, rewritten, "utf8");
}

console.log(`[brand-prebuild] BRAND_ID=${BRAND_ID}`);
console.log(`[brand-prebuild]   assets   → ${PUBLIC_BRAND_DIR.replace(ROOT, ".")}`);
console.log(`[brand-prebuild]   config   → ${ACTIVE_TS_PATH.replace(ROOT, ".")}`);
console.log(`[brand-prebuild]   manifest → ${MANIFEST_PATH.replace(ROOT, ".")}`);
console.log(`[brand-prebuild]   helpdocs → ${HELPDOCS_DEST.replace(ROOT, ".")}`);
console.log(`[brand-prebuild]   legal    → ${LEGAL_DEST.replace(ROOT, ".")}`);
console.log(`[brand-prebuild]   landing  → ${existsSync(LANDING_SRC) ? LANDING_DEST.replace(ROOT, ".") : "(pack has none — skipped)"}`);
